package com.aoshi.domain;

import java.math.BigDecimal;
import java.util.List;

public class AsProduct {
    private Integer productId;

    private Integer goodsId;

    private String productSn;

    private Integer productStore;

    private BigDecimal productPrice;
    
    private String goodsAttr;

    private String attrName;
    
  //页面显示数据
    private List<String> goodsAttrNames;
    private List<String> goodsAttrValues;

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public Integer getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Integer goodsId) {
        this.goodsId = goodsId;
    }

    public String getProductSn() {
        return productSn;
    }

    public void setProductSn(String productSn) {
        this.productSn = productSn == null ? null : productSn.trim();
    }

    public Integer getProductStore() {
        return productStore;
    }

    public void setProductStore(Integer productStore) {
        this.productStore = productStore;
    }

    public BigDecimal getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(BigDecimal productPrice) {
        this.productPrice = productPrice;
    }

	public String getGoodsAttr() {
		return goodsAttr;
	}

	public void setGoodsAttr(String goodsAttr) {
		this.goodsAttr = goodsAttr;
	}

	public String getAttrName() {
		return attrName;
	}

	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}

	public List<String> getGoodsAttrNames() {
		return goodsAttrNames;
	}

	public void setGoodsAttrNames(List<String> goodsAttrNames) {
		this.goodsAttrNames = goodsAttrNames;
	}

	public List<String> getGoodsAttrValues() {
		return goodsAttrValues;
	}

	public void setGoodsAttrValues(List<String> goodsAttrValues) {
		this.goodsAttrValues = goodsAttrValues;
	}
    
}