package com.aoshi.domain;

public class AsCooperation {
    private Integer cooperationId;

    private Integer activeId;

    private Integer unactiveId;

    private AsStore store;
    
    private AsShop shop;
    

    public AsShop getShop() {
		return shop;
	}

	public void setShop(AsShop shop) {
		this.shop = shop;
	}

	public AsStore getStore() {
        return store;
    }

    public void setStore(AsStore store) {
        this.store = store;
    }

    public Integer getCooperationId() {
        return cooperationId;
    }

    public void setCooperationId(Integer cooperationId) {
        this.cooperationId = cooperationId;
    }

    public Integer getActiveId() {
        return activeId;
    }

    public void setActiveId(Integer activeId) {
        this.activeId = activeId;
    }

    public Integer getUnactiveId() {
        return unactiveId;
    }

    public void setUnactiveId(Integer unactiveId) {
        this.unactiveId = unactiveId;
    }
}