package com.aoshi.common.asenum;

/**
 * 基本类型
 * 
 * @author huangxw
 * @date 2016年10月25日
 */
public enum baseTypeEnum {

	INDUSTRY(1, "行业"), MODULE(2, "模块"), PRIZE(3, "奖品"), Discount(4, "优惠券"),STORE(5, "商场类型");

	private int id;
	private String remark;

	private baseTypeEnum(int id, String remark) {
		this.id = id;
		this.remark = remark;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
