package com.aoshi.common.asenum;

/**
 * 活动类型枚举
 *
 * @author tgb 创建时间：2016年11月5日
 * @version 1.0
 */
public enum ActivityTypeEnum {
	
	DRAW_TYPE(1, "抽奖"), SHAKE_TYPE(2, "摇奖"), COUPON_TYPE(3, "优惠券"), OWN_TYPE(4, "自主活动");

	private int id;
	private String remark;

	private ActivityTypeEnum(int id, String remark) {
		this.id = id;
		this.remark = remark;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
